package com.evolveum.midpoint.tools.ninja;

/**
 * @author lazyman
 */
public class ImportDDLConfig {

    private String driver;
    private String url;
    private String username;
    private String password;
    private boolean promptForPassword;

    private String filePath;

    public String getDriver() {
        return driver;
    }

    public void setDriver(String driver) {
        this.driver = driver;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public boolean isPromptForPassword() {
        return promptForPassword;
    }

    public void setPromptForPassword(boolean promptForPassword) {
        this.promptForPassword = promptForPassword;
    }
}
